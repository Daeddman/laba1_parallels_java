#!/bin/bash
cd "$(dirname "$0")/src"
javac Main.java
if [ $? -eq 0 ]; then
    echo "✅ Компіляція успішна!"
    echo ""
    echo "Тест: 3 потоки з різним часом роботи"
    echo "Потік 1: крок=5, час=2000мс (найдовший)"
    echo "Потік 2: крок=10, час=500мс (найкоротший - завершиться першим!)"
    echo "Потік 3: крок=3, час=1000мс (середній)"
    echo ""
    echo -e "3\n5\n2000\n10\n500\n3\n1000" | java Main
else
    echo "❌ Помилка компіляції!"
fi

