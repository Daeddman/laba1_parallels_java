#!/bin/bash

# Скрипт для автоматичного тестування програми з різними параметрами

echo "=== Автоматичне тестування багатопоточної програми ==="
echo ""

# Перевірка компіляції
echo "Компіляція програми..."
cd "$(dirname "$0")/src"
javac Main.java

if [ $? -ne 0 ]; then
    echo "Помилка компіляції!"
    exit 1
fi

echo "Компіляція успішна!"
echo ""

# Тест 1: 3 потоки - демонстрація незалежного завершення
echo "==================== ТЕСТ 1: Незалежне завершення потоків ===================="
echo "Потік 1: крок=5, час=2000мс (найдовший)"
echo "Потік 2: крок=10, час=500мс (найкоротший - завершиться ПЕРШИМ!)"
echo "Потік 3: крок=3, час=1000мс (середній)"
echo -e "3\n5\n2000\n10\n500\n3\n1000" | java Main
echo ""
sleep 2

# Тест 2: 4 потоки з різним часом
echo "==================== ТЕСТ 2: 4 потоки з різним часом ===================="
echo "Потік 1: крок=2, час=1500мс"
echo "Потік 2: крок=4, час=300мс (завершиться першим)"
echo "Потік 3: крок=6, час=800мс"
echo "Потік 4: крок=8, час=2000мс (завершиться останнім)"
echo -e "4\n2\n1500\n4\n300\n6\n800\n8\n2000" | java Main
echo ""
sleep 2

# Тест 3: 2 потоки - простий приклад
echo "==================== ТЕСТ 3: 2 потоки ===================="
echo "Потік 1: крок=5, час=1000мс"
echo "Потік 2: крок=10, час=600мс (завершиться швидше)"
echo -e "2\n5\n1000\n10\n600" | java Main
echo ""

echo "=== Всі тести завершено ==="

